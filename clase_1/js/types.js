//tipos de variables de js
//tipo numero -> var variable = numero;
var variable = 0;
console.log(variable);
//tipo string -> var variable = "numero";
variable = "hola a todos";
console.log(variable);
//tipo boolean -> var variable = true | false;
variable = true;
console.log(variable);
/*tipo objeto -> var variable = {
    propiedad: valor,
    proedad2: valor2
};*/
variable = {};
console.log(variable);
//tipo array -> var variable = [valor, valor2, ..., valorn]
variable = [];
console.log(variable);
//tipo null -> var variable = null
variable = null;
console.log(variable);
//tipo undefined -> var variable = undefined
variable = undefined;
console.log(variable);